const char *const CGaonNuriApp::GetApplicationPath()
{
	// 모든 데이타 path는 현재 디렉토리의 ..의 sub디렉토리 이다
	static BOOL fInit = FALSE;

	if (fInit == FALSE) {
		fInit = TRUE;

		::GetCurrentDirectory(_MAX_PATH, m_strExePath);
		sprintf_s(m_strAppPath, "%s\\..\\", m_strExePath);
	}

	return m_strAppPath;
}